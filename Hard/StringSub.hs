replaceSub::String->Int->Int->String->String
replaceSub source index len replacement = pre ++ replacement ++ post
  where
    pre = take index source
    post = drop (index+len) source


stringSubstitute::(String,String)-> (String,String)->(String,String)
stringSubstitute (result,mask) (find,replace)
  | isNothing mindex = (result,mask)
  | otherwise = stringSubstitute (newresult,newmask) (find,replace)
  where
    mindex = elemIndex find $ map (\x-> take (length find) $ drop x mask) [0..length mask - length find]
    index = fromJust mindex
    newresult = replaceSub result index (length find) replace
    newmask =  replaceSub mask index (length find) (replicate (length replace) '#')

stringSubstitution::String->String
stringSubstitution line = fst $ foldl stringSubstitute (s,s) findReplaceArgs
  where
    args = splitOn ";" line
    s = head args
    findArgs = splitOn "," (args!!1)
    getfindArg n = (findArgs!!(n*2),findArgs!!(1+ (n*2)))
    findReplaceArgs = map getfindArg [0..div (length findArgs) 2 -1]

